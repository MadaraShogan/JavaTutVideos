package JAVA_VIDEOS.java_video13;

public class Diff_ways_print {
    public static void main(String[] args){
        int a=5;
        float b=10.294820f;
        System.out.println("A="+a+" AND B="+b);
        System.out.printf("A=%d AND B=%f\n",a,b);
        System.out.format("A=%d AND B=%f\n",a,b);
        System.out.printf("A=%d AND B=%.3f\n",a,b);
        System.out.printf("A=%d AND B=%9.1f\n",a,b);
    }
}
