package JAVA_VIDEOS.java_video18;

public class Switch {
    public static void main(String[] args) {
        String var="A";
        switch (var) {
            case "B":
                System.out.println("HELLO1");
                break;
            case "C":
                System.out.println("HELLO2");
            default:
                System.out.println("HELLO3");
                break;
        }
    }
}
